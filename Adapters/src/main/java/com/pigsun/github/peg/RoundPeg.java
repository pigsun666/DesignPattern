package com.pigsun.github.peg;

/**
 * 圆钉
 * @author pigsun
 */
public class RoundPeg {

    /**
     * 半径
     */
    private double radius;

    public RoundPeg() {
    }

    public RoundPeg(double radius) {
        this.radius = radius;
    }

    public double getRadius() {
        return radius;
    }
}
